# 两个整数之间的汉明距离指的是这两个数字对应二进制位不同的位置的数目。
#
# 给出两个整数 x 和 y，计算它们之间的汉明距离。


class Solution(object):
    def hammingDistance(self, x, y):
        """
        :type x: int
        :type y: int
        :rtype: int
        """
        return bin(x ^ y).count('1')  # ^ 异或


if __name__ == '__main__':
    ob = Solution()
    print(ob.hammingDistance(1, 4))

    print(1 ^ 4)